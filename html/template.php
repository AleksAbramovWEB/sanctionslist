<h2>Список OFAC SDN</h2>



<p>Включение в список Specially Designated Nationals And Blocked Persons List&nbsp;(SDN) означает, что все «имущество и права на имущество» лица из списка SDN, которые находятся или попадают во владение или под контроль лица США или на территории США, блокируются, и сделки с ними запрещены. Юридические лица, прямо или косвенно находящиеся во владении одного или нескольких заблокированных лиц на 50% или более, по отдельности или совместно, также считаются заблокированными в силу закона. В отсутствие лицензии или другого разрешения от OFAC все операции с лицом в списке SDN (а также любыми компаниями, которые на 50% или более находятся во владении, прямо или косвенно, одного или нескольких лиц из списка SDN) запрещены для лиц США, как и любые сделки, в которых участвует посредник США.</p>




